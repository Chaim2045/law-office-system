/**
 * business-rules-adapter.js — IIFE adapter (byte-identical sibling)
 *
 * Mirrors shared/business-rules/service-classification.js for browser consumption.
 * Exposes window.BUSINESS_RULES with three predicates: isFixedService,
 * isHourlyService, isLegalProcedureService.
 *
 * This file exists at TWO paths, BYTE-IDENTICAL:
 *   - apps/admin-panel/js/shared/business-rules-adapter.js
 *   - apps/user-app/js/shared/business-rules-adapter.js
 *
 * Both are kept in sync with the canonical SOT via
 * tests/unit/shared/business-rules.sync.test.ts (function-output matrix).
 *
 * Load order: this script MUST be loaded BEFORE any consumer of
 * window.BUSINESS_RULES (e.g. client-type-display.js).
 *
 * DO NOT EDIT THIS FILE DIRECTLY. Update shared/business-rules/service-classification.js
 * and mirror the change to BOTH adapter copies. The sync test will fail otherwise.
 */

(function () {
  'use strict';

  function isFixedService(svc) {
    if (!svc || typeof svc !== 'object' || !svc.type) {
      return false;
    }
    return svc.type === 'fixed' ||
      (svc.type === 'legal_procedure' && svc.pricingType === 'fixed');
  }

  function isHourlyService(svc) {
    if (!svc || typeof svc !== 'object' || !svc.type) {
      return false;
    }
    return svc.type === 'hours' ||
      (svc.type === 'legal_procedure' && svc.pricingType === 'hourly');
  }

  function isLegalProcedureService(svc) {
    if (!svc || typeof svc !== 'object' || !svc.type) {
      return false;
    }
    return svc.type === 'legal_procedure';
  }

  const api = {
    isFixedService: isFixedService,
    isHourlyService: isHourlyService,
    isLegalProcedureService: isLegalProcedureService
  };

  if (typeof window !== 'undefined') {
    window.BUSINESS_RULES = api;
  } else if (typeof globalThis !== 'undefined') {
    globalThis.BUSINESS_RULES = api;
  }
})();
